package com.example.tennis_padel;

// This is the class of the Tennis-Padel club that allows to set and get the name and the logo
public class Club {
    private String clubLogo, clubName;

    public String getClubLogo() {return clubLogo;}

    public void setClubLogo(String clubLogo) {this.clubLogo = clubLogo;}

    public String getClubName() {return clubName;}

    public void setClubName(String clubName) {this.clubName = clubName;}
}
